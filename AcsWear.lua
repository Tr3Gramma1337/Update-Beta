function isPresetKeyValid(key)
    return key:find("(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+);(.+);(.+)")
end

function getKeyItemList(key)
    if key:find("(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+);(.+);(.+)") then
        s1, c1, s2, c2, s3, c3, s4, c4, s5, c5, s6, c6, armour, armour_color, skin, fight, walk =
            key:match("(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+):(.+);(.+);(.+);(.+)")

        tbl = {s1, s2, s3, s4, s5, s6, armour, armour_color, skin, fight, walk}
        return tbl
    end
end

