function string.rlower(s)
    s = s:lower()
    local strlen = s:len()
    if strlen == 0 then
        return s
    end
    s = s:lower()
    local output = ""
    for i = 1, strlen do
        local ch = s:byte(i)
        if ch >= 192 and ch <= 223 then
            output = output .. russian_characters[ch + 32]
        elseif ch == 168 then
            output = output .. russian_characters[184]
        else
            output = output .. string.char(ch)
        end
    end
    return output
end

